package entity;

public class Inscrito {

	private String nome;
	private String email;
	private String rg;
	private String cpf;
	private String opcCurso;
	private String telefone;
	private String nomeFaculdade;
	private String nomeCurso;
	private Double mediaFaculdade;
	private String curriculo;
	
	public String getNomeFaculdade() {
		return nomeFaculdade;
	}

	public void setNomeFaculdade(String nomeFaculdade) {
		this.nomeFaculdade = nomeFaculdade;
	}

	public String getNomeCurso() {
		return nomeCurso;
	}

	public void setNomeCurso(String nomeCurso) {
		this.nomeCurso = nomeCurso;
	}

	public Double getMediaFaculdade() {
		return mediaFaculdade;
	}

	public void setMediaFaculdade(Double mediaFaculdade) {
		this.mediaFaculdade = mediaFaculdade;
	}

	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getRg() {
		return rg;
	}
	
	public void setRg(String rg) {
		this.rg = rg;
	}
	
	public String getCpf() {
		return cpf;
	}
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	
	public String getOpcCurso() {
		return opcCurso;
	}
	
	public void setOpcCurso(String opcCurso) {
		this.opcCurso = opcCurso;
	}
	
	public String getTelefone() {
		return telefone;
	}
	
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	
	public String getCurriculo() {
		return curriculo;
	}
	
	public void setCurriculo(String curriculo) {
		this.curriculo = curriculo;
	}

	
	
}
